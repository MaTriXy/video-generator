"""Resolve node_modules paths for monorepo compatibility.

In monorepos with npm workspaces, packages like @iconify/json and react-icons
may be hoisted to the root node_modules. This module checks the app's local
node_modules first, then falls back to the monorepo root.

Built index files are always written to the app's own node_modules.
"""

import os

_APP_DIR = os.path.dirname(os.path.dirname(os.path.dirname(os.path.abspath(__file__))))
_LOCAL_NODE_MODULES = os.path.join(_APP_DIR, "node_modules")
_ROOT_NODE_MODULES = os.path.join(os.path.dirname(os.path.dirname(_APP_DIR)), "node_modules")


def _resolve(rel_path: str) -> str:
    """Check local node_modules first, then root. Returns the node_modules dir."""
    local = os.path.join(_LOCAL_NODE_MODULES, rel_path)
    if os.path.exists(local):
        return _LOCAL_NODE_MODULES
    root = os.path.join(_ROOT_NODE_MODULES, rel_path)
    if os.path.exists(root):
        return _ROOT_NODE_MODULES
    return _LOCAL_NODE_MODULES


# Source libraries — resolved independently (may live in different node_modules)
_ICONIFY_NM = _resolve(os.path.join("@iconify", "json"))
_REACT_ICONS_NM = _resolve("react-icons")

ICONIFY_JSON_PATH = os.path.join(_ICONIFY_NM, "@iconify", "json", "json")
ICONIFY_PKG_PATH = os.path.join(_ICONIFY_NM, "@iconify", "json", "package.json")
REACT_ICONS_PATH = os.path.join(_REACT_ICONS_NM, "react-icons")

# Built index files always go in the app's own node_modules
INDEX_FILE_PATH = os.path.join(_LOCAL_NODE_MODULES, ".icon_index.bin")
LIBRARY_ICONS_FILE_PATH = os.path.join(_LOCAL_NODE_MODULES, ".library_icons.bin")
